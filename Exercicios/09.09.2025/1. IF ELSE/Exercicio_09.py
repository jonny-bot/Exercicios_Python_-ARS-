Numero_1 = int(input('Digite o 1° Número: '))

Numero_2 = int(input('Digite o 2° Número: '))

Sinal = input('Digite um Sinal: ')

if Sinal == '+':

    Resultado = Numero_1 + Numero_2

    print(f'A Soma dos Números dá {Resultado}')

elif Sinal == '-':

    Resultado = Numero_1 - Numero_2

    print(f'A Subtração dos Números dá {Resultado}')

elif Sinal == '*':

    Resultado = Numero_1 * Numero_2

    print(f'A Multiplicação dos Números dá {Resultado}')

elif Sinal == '/':

    Resultado = Numero_1 / Numero_2

    print(f'A Divisão dos Números dá {Resultado}')

